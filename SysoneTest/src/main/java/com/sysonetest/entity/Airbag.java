package com.sysonetest.entity;


class Airbag {
    int precio;
    String nombre;

    public Airbag() {
        precio =7000;
        nombre ="AB";
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
